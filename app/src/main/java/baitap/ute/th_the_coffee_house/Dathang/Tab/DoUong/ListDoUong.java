package baitap.ute.th_the_coffee_house.Dathang.Tab.DoUong;

public class ListDoUong {

    int imgtu;
    String nametu,giatu;

    public ListDoUong(int imgtu, String nametu, String giatu) {
        this.imgtu = imgtu;
        this.nametu = nametu;
        this.giatu = giatu;
    }

    public int getImgtu() {
        return imgtu;
    }

    public String getNametu() {
        return nametu;
    }

    public String getGiatu() {
        return giatu;
    }
}


